console.log("This is a popup message from popups.ts file.");
alert("Hello");
confirm("Are you sure you want to proceed?");
var data = prompt("Please enter your name:", "Guest");
console.log("User input from prompt: " + data);
// here alert, confirm, and prompt are used to demonstrate popup messages in TypeScript.
// alert displays a message to the user.
// confirm displays a message and waits for the user to click "OK" or "Cancel".
// prompt displays a message and an input field for the user to enter data.
// The values returned by confirm and prompt can be captured and used in the program.
