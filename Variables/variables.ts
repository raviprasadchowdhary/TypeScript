// Declaring a variable with type annotation

// This variable 'n1' is of type number
var n1:number = 10;
console.log(n1);

// This is how to declare string variables using different quotation styles
var s1: string = "Hello, Typescript! Double quotes";
var s2: string = 'Hello, Typescript! Single quotes';
var s3: string = `Hello, Typescript! Backticks`;

console.log(s1);
console.log(s2);
console.log(s3);

// Declaring boolean variables
var b1: boolean = true;
var b2: boolean = false;

console.log(b1);
console.log(b2);